package cc.mrbird.web.domain;

public class SecondHouseDetail {

    private String  device_id;
    private String city;
    private String blockshowname;
    private String averprice_x;
    private String price;
    private String buildarea;


    public String getDevice_id() {
        return device_id;
    }

    public void setDevice_id(String device_id) {
        this.device_id = device_id;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBlockshowname() {
        return blockshowname;
    }

    public void setBlockshowname(String blockshowname) {
        this.blockshowname = blockshowname;
    }

    public String getAverprice_x() {
        return averprice_x;
    }

    public void setAverprice_x(String averprice_x) {
        this.averprice_x = averprice_x;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getBuildarea() {
        return buildarea;
    }

    public void setBuildarea(String buildarea) {
        this.buildarea = buildarea;
    }
}
